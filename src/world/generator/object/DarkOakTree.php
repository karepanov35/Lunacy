	public function canPlaceObject(ChunkManager $world, int $x, int $y, int $z, Random $random) : bool{
		// Check if there's enough space (20 blocks high)
		for($yy = 0; $yy <= 20; $yy++){
			$block = $world->getBlockAt($x, $y + $yy, $z);
			if(!$block->canBeReplaced()){
				return false;
			}
		}
		return true;
	}
